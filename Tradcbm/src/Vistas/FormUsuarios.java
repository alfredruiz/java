    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
    try{  
        //Validamos los campos
        //Valor seleccionado del combobox
        int selec = cbmperfil.getSelectedIndex();
        
        if (txtidusuarios.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un usuario", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            txtidusuarios.requestFocus();
            return;
        }
        if (txtnombre.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            txtnombre.requestFocus();
            return;
        }
        if (txtapellidos.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellidos", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            txtapellidos.requestFocus();
            return;
        }
        if (txtemail.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un email", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            txtemail.requestFocus();
            return;
        }
        if (txtpassword.getPassword().length == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar password", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            txtpassword.requestFocus();
            return;
        }
        if (txtpassword2.getPassword().length == 0 ){
            JOptionPane.showConfirmDialog(rootPane, "Deben repetir el valor de password", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            txtpassword2.requestFocus();
            return;
        }
        if (!Arrays.equals(txtpassword2.getPassword(), txtpassword.getPassword())){
            JOptionPane.showConfirmDialog(rootPane, "Deben ser iguales los valores de password", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            txtpassword2.requestFocus();
            return;
        }            
        if (selec == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes seleccionar un perfil", "Acceso a Usuarios", JOptionPane.ERROR_MESSAGE);
            cbmperfil.requestFocus(); 
            return;
        }
        //Instanciamos ambas clases
        usu = new DatosUsuario();
        usuario = new Usuarios(); 
        //Recogo los datos de las cajas de texto
        usuario.setIdusuarios(txtidusuarios.getText());
        usuario.setNombre(txtnombre.getText());
        usuario.setApellidos(txtapellidos.getText());
        usuario.setEmail(txtemail.getText());
        usuario.setPassword(new String(txtpassword.getPassword()));
        usuario.setPassword(new String(txtpassword2.getPassword()));
        usuario.setPerfil(cbmperfil.getItemAt(selec));
        //Si la accion es guardar , ingreso el nuevo usuario y muestro el mensaje
        if (acc.equals("guardar")) {
            if (usu.nuevoUsuario(usuario)) {
               JOptionPane.showMessageDialog(rootPane, "Usuario ingresado correctamente");
               //Muestro los datos del nuevo usuario en la tabla
               MostrarDatosTabla("");
               //Inhabilito los campos
               inhabilitar();
            }
         //Si por el contrario la acción es editar. Tomamos el id del usuario
        }else if (acc.equals("editar")) {
           usuario.setIdusuarios(txtidusuarios.getText());
            //Modificamos el usuario y mostramos mensaje
            if (usu.modificarUsuario(usuario)) {
               JOptionPane.showMessageDialog(rootPane, "Usuario modificado correctamente");
               //Mostramos los datos modificados
               MostrarDatosTabla("");
               //Inhabilitamos datos
               inhabilitar();
            }
        } 
    }catch (HeadlessException e) {
             JOptionPane.showConfirmDialog(null,e);
        }
    }//GEN-LAST:event_btnguardarActionPerformed

