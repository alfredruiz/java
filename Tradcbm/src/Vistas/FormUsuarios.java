    private void tablausuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablausuMouseClicked
       //Cuando haga click en la fila de la tabla los datos pasan a sus cajas de texto 
       //El botón guardar cambia el texto a Editar y la acción pasa a ser editar
       btnguardar.setText("Editar");
       //El botón borrar se inhabilita
       btnborrar.setEnabled(true);
       //Habilitamos los campos
       habilitar();
       //La acción pasa a ser editar
       acc="editar";
       //Inhabilito el idusuarios al no poder modificarse por ser clave primaria
       txtidusuarios.setEnabled(false);
       //Capturamos la fila q ha sido cliqueada
       int fila= tablausu.rowAtPoint(evt.getPoint());
         //Tomamos los datos de la tabla y los pasamos a sus cajas de texto
        txtidusuarios.setText(tablausu.getValueAt(fila, 0).toString());
        txtnombre.setText(tablausu.getValueAt(fila, 1).toString());
        txtapellidos.setText(tablausu.getValueAt(fila, 2).toString());
        txtemail.setText(tablausu.getValueAt(fila, 3).toString());
        txtpassword.setText(tablausu.getValueAt(fila, 4).toString());
        txtpassword2.setText(tablausu.getValueAt(fila, 5).toString());
        cbmperfil.setSelectedItem(tablausu.getValueAt(fila, 6).toString());
 
    }//GEN-LAST:event_tablausuMouseClicked

